sizeVer(double height) {
  return SizedBox(
    height: height,
  );
}

sizeHor(double width) {
  return SizedBox(
    width: width,
  );
}

sizedBox(double height, double width) {
  return SizedBox(
    width: width,
    height: height,
  );
}
